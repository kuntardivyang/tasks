# Convert a list to a tuple and vice versa.


def activity_1():
    
    list_one = [1,2,3,4,5,6,7,8,9]
    tuple_one = tuple(list_one)
    print('List : ', list_one)
    print('After converting into tuple : ', tuple_one)
    print('converting back to list : ', list(tuple_one) )
    
activity_1()